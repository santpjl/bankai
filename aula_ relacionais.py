#operadores relacionais
a = 5
b = 2
#comparacao >Maior que
maior = a > b
print(maior)

#comparacao <MEnor que
menor = a < b 
print(menor)

#comparacao == igualdade
igual = a == b 
print(igual)

#comparacao != diferente
diferente = a!= b 
print(diferente)

#comparacao >= maior igual 
maiorIgual= a >= b 
print(maiorIgual)

#comparacao <= menor igual 
menorIgual= a <= b 
print(menorIgual)

